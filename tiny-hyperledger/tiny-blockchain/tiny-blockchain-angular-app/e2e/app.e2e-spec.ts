import { AngularTestPage } from './app.po';
import { ExpectedConditions, browser, element, by } from 'protractor';
import {} from 'jasmine';


describe('Starting tests for tiny-blockchain-angular-app', function() {
  let page: AngularTestPage;

  beforeEach(() => {
    page = new AngularTestPage();
  });

  it('website title should be tiny-blockchain-angular-app', () => {
    page.navigateTo('/');
    return browser.getTitle().then((result)=>{
      expect(result).toBe('tiny-blockchain-angular-app');
    })
  });

  it('network-name should be tiny-blockchain@0.0.2-deploy.0',() => {
    element(by.css('.network-name')).getWebElement()
    .then((webElement) => {
      return webElement.getText();
    })
    .then((txt) => {
      expect(txt).toBe('tiny-blockchain@0.0.2-deploy.0.bna');
    });
  });

  it('navbar-brand should be tiny-blockchain-angular-app',() => {
    element(by.css('.navbar-brand')).getWebElement()
    .then((webElement) => {
      return webElement.getText();
    })
    .then((txt) => {
      expect(txt).toBe('tiny-blockchain-angular-app');
    });
  });

  
    it('Shipment component should be loadable',() => {
      page.navigateTo('/Shipment');
      browser.findElement(by.id('assetName'))
      .then((assetName) => {
        return assetName.getText();
      })
      .then((txt) => {
        expect(txt).toBe('Shipment');
      });
    });

    it('Shipment table should have 5 columns',() => {
      page.navigateTo('/Shipment');
      element.all(by.css('.thead-cols th')).then(function(arr) {
        expect(arr.length).toEqual(5); // Addition of 1 for 'Action' column
      });
    });
  

  
    it('Business component should be loadable',() => {
      page.navigateTo('/Business');
      browser.findElement(by.id('participantName'))
      .then((participantName) => {
        return participantName.getText();
      })
      .then((txt) => {
        expect(txt).toBe('Business');
      });
    });

    it('Business table should have 3 columns',() => {
      page.navigateTo('/Business');
      element.all(by.css('.thead-cols th')).then(function(arr) {
        expect(arr.length).toEqual(3); // Addition of 1 for 'Action' column
      });
    });
  

  
    it('shipmentTransaction component should be loadable',() => {
      page.navigateTo('/shipmentTransaction');
      browser.findElement(by.id('transactionName'))
      .then((transactionName) => {
        return transactionName.getText();
      })
      .then((txt) => {
        expect(txt).toBe('shipmentTransaction');
      });
    });
  

});